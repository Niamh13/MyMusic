    private boolean search(String name, LinearInterface playlist){
        // Search list
        boolean visi = false;
        
        //for searching in playlist DLL
            for(int j = 0; j<playlist.size(); j++){
               if(playlist.getItem(j).contains(name)){
                    visi = true;
                    break;
                }
                else{
                    visi = false;
                } 
            }
        
        return visi;
    }
