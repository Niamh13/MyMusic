    private void add(String name, LinearInterface playlist){
        if(!search(name, playlist)){
            likedStack.push(name);
            playlist.add(1, name);
        }
        else{
            System.out.println("Already in playlist");
        }
    }
    
    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // add song to liked playlist and selected genre
        String name = nameTF.getText();
        if(soundRBTN.isSelected()){
            add(name, soundL);
        }
        else if(popRBTN.isSelected()){
            add(name,popL);
        }
        else{
            System.out.println("Pick genre:");
        }
        
        JOptionPane.showMessageDialog(null, "Song entered successfully");
        nameTF.setText("");
        
    }//GEN-LAST:event_addBTNActionPerformed
    private void delete(String name, LinearInterface playlist){
        if(search(name, playlist)){
            //for deleting in playlist DLL
            for(int j = 0; j<(playlist.size()); j++){
                if(playlist.getItem(j).contains(name)){
                        playlist.remove(j);
                        break;
                    }
                }
               
            // Deleting for liked list
            likedStack.pop();
        }
        else{
            System.out.println("Not in the list");
        }
        
        
    }
