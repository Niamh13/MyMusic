    private void move(String name, LinearInterface playlist, LinearInterface orgPlaylist){
        if(search(name, orgPlaylist)){
            delete(name, orgPlaylist);
            add(name, playlist);
        }
        else{
            JOptionPane.showMessageDialog(null, "Song not in playlist");
        }
    }
    
